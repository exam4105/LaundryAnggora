function handleCheckboxClickOne(checkbox) {
  var checkboxes = document.getElementsByName("optionone");
  for (var i = 0; i < checkboxes.length; i++) {
    if (checkboxes[i] !== checkbox) {
      checkboxes[i].checked = false;
    }
  }
}
